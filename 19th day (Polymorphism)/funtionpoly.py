#Function polymorphism
#len function

#1. For String - It returns number of characters

text = "Aryan"
print(len(text))

#2. For Tuple - It returns number of items

t1 = ("Apple","Banana","Cherry")
print(len(t1))

#3. For Dictionary - It returns number of key/value pair

d1 = {
    "rollno":1,
    "name":"Aryan",
    "age":21
    }

print(len(d1))
